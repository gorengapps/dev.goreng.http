namespace Http
{
    /// <summary>
    /// Specifies the HTTP method to use for a request.
    /// </summary>
    public enum HttpMethod
    {
        /// <summary>
        /// HTTP GET method - used to retrieve data from the server.
        /// </summary>
        Get,
        
        /// <summary>
        /// HTTP POST method - used to send data to the server.
        /// </summary>
        Post
    }
}